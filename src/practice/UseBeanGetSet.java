package practice;

public class UseBeanGetSet {
	private String nm;
	private String em;
	private String ps;
	
	public UseBeanGetSet() {
	}

	public String getNm() {
		return nm;
	}

	public void setNm(String nm) {
		this.nm = nm;
	}

	public String getEm() {
		return em;
	}

	public void setEm(String em) {
		this.em = em;
	}

	public String getPs() {
		return ps;
	}

	public void setPs(String ps) {
		this.ps = ps;
	}
	
	
	
	
}
